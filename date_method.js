//     How to create Date()
let date=new Date()
console.log(date);


//    now()
console.log(Date.now());


//    getDate()
console.log(date.getDate());


//    getMonth()
console.log(date.getMonth());  // month index starting with zero


//    getDay()
console.log(date.getDay());   // 1 index = 'monday'


//    getFullYear()
console.log(date.getFullYear());


//    getHours()
console.log(date.getHours());


//    getMinutes()
console.log(date.getMinutes());


//    getTime()
console.log(date.getTime());


//    toDateString()
console.log(date.toDateString());

